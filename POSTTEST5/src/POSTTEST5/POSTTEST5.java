    private void jupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jupdateActionPerformed
        // TODO add your handling code here:
         if(JTnama.getText().length()==0 || JTno.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Nama dan No Telepon KOSONG");
        }
        else{
        int baris = jtable.getSelectedRow();
        
        jtable.setValueAt(JTnama.getText(), baris, 0);
        jtable.setValueAt(JTno.getText(), baris, 1);
        jtable.setValueAt(Jkota.getSelectedItem(), baris, 2);
    }//GEN-LAST:event_jupdateActionPerformed
    }
    private void jtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtableMouseClicked
        // TODO add your handling code here:
         int baris = jtable.getSelectedRow();
        
        String nama_edit = jtable.getValueAt(baris, 0).toString();
        String notelp_edit = jtable.getValueAt(baris, 1).toString();
        String kotaasal_edit = jtable.getValueAt(baris, 2).toString();
        
        JTnama.setText(nama_edit);
        JTno.setText(notelp_edit);
        Jkota.setSelectedItem(kotaasal_edit);
    }//GEN-LAST:event_jtableMouseClicked

