    private void JTnoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JTnoKeyPressed
        // TODO add your handling code here:
         JTno.addKeyListener(new KeyAdapter(){
             public void keyTyped(KeyEvent e){
                 char karakter = e.getKeyChar();
                 if(!((karakter >='0') && (karakter<='9') || (karakter == '\b'))){
                     e.consume();
                 }
             }
        });
    }//GEN-LAST:event_JTnoKeyPressed

